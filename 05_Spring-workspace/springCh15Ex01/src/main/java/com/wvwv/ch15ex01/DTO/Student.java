package com.wvwv.ch15ex01.DTO;


public class Student {
	private String name;
	private int id;
	
	public Student() {
		super();
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public int getId() {
		return id;
	}
	
	public void setId(int id) {
		this.id = id;
	}
	
	
}
