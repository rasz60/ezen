package com.wvwv.ch08ex02;

public class AdminConnection {
	private String adminId;
	private String adminPw;
	private String adminSubId;
	private String adminSubPw;	

	public String getAdminId() {
		return adminId;
	}

	public void setAdminId(String adminId) {
		this.adminId = adminId;
	}

	public String getAdminPw() {
		return adminPw;
	}

	public void setAdminPw(String adminPw) {
		this.adminPw = adminPw;
	}
	
	public String getAdminSubPw() {
		return adminSubPw;
	}

	public void setAdminSubPw(String adminSubPw) {
		this.adminSubPw = adminSubPw;
	}

	public String getAdminSubId() {
		return adminSubId;
	}

	public void setAdminSubId(String adminSubId) {
		this.adminSubId = adminSubId;
	}
	
	
}
